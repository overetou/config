#include "libft.h"

void	swap_ptr(void **p1, void **p2)
{
	void	*p3;

	p3 = *p1;
	*p1 = *p2;
	*p2 = p3;
}

void	swap_int(int *a, int *b)
{
	*a += *b;
	*b = *a - *b;
	*a -= *b;
}
